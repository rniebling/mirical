package de.nieblings.zipato.data.attribut;

public class UiType
{
  private String link;

  public String getLink() { return this.link; }

  public void setLink(String link) { this.link = link; }

  private String name;

  public String getName() { return this.name; }

  public void setName(String name) { this.name = name; }

  private String endpointType;

  public String getEndpointType() { return this.endpointType; }

  public void setEndpointType(String endpointType) { this.endpointType = endpointType; }

  private String relativeUrl;

  public String getRelativeUrl() { return this.relativeUrl; }

  public void setRelativeUrl(String relativeUrl) { this.relativeUrl = relativeUrl; }
}

